    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {
            Vehiculo v = leerForm();
            if (vehiculoDAO.exists(v.getDominio())) vehiculoDAO.update(v); else vehiculoDAO.insert(v);
            cargarTabla();
            JOptionPane.showMessageDialog(this, "Guardado OK");
            txtDominio.setEditable(false);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int row = tblVehiculos.getSelectedRow();
        String dom = row>=0 ? String.valueOf(modelVeh.getValueAt(row, 0)) : txtDominio.getText().trim().toUpperCase();
        if (dom.isEmpty()) { JOptionPane.showMessageDialog(this, "Seleccione o escriba un dominio."); return; }
        if (JOptionPane.showConfirmDialog(this, "多Eliminar " + dom + "?", "Confirmar", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION) {
            try {
                vehiculoDAO.delete(dom);
                cargarTabla();
                limpiarForm();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tblVehiculosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblVehiculosMouseClicked
        // TODO add your handling code here:
        int row = tblVehiculos.getSelectedRow();
        if (row >= 0) {
            txtDominio.setText(String.valueOf(modelVeh.getValueAt(row, 0)));
            Object a = modelVeh.getValueAt(row, 1); txtAnio.setText(a==null? "" : a.toString());
            txtMarca.setText(String.valueOf(modelVeh.getValueAt(row, 2)));
            txtModelo.setText(String.valueOf(modelVeh.getValueAt(row, 3)));
            txtColor.setText(String.valueOf(modelVeh.getValueAt(row, 4)));
            txtDominio.setEditable(false);
        }
    }//GEN-LAST:event_tblVehiculosMouseClicked

