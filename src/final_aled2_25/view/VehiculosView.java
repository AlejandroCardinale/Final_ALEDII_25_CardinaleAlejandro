    @Override
    public void addNotify() {
        super.addNotify();
        if (modelVeh == null) {
            modelVeh = new DefaultTableModel(new Object[][]{}, new String[]{"Dominio","A単o","Marca","Modelo","Color"}) {
                @Override public boolean isCellEditable(int r, int c) { return false; }
            };
            tblVehiculos.setModel(modelVeh);
            tblVehiculos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            cargarTabla();
        }
    }

    private void cargarTabla() {
        modelVeh.setRowCount(0);
        List<Vehiculo> lista = vehiculoDAO.findAll();
        for (Vehiculo v : lista) {
            modelVeh.addRow(new Object[]{
                v.getDominio(), v.getAnio(), v.getMarca(), v.getModelo(), v.getColor()
            });
        }
    }

    private Vehiculo leerForm() throws Exception {
        Vehiculo v = new Vehiculo();
        String dom = txtDominio.getText().trim().toUpperCase();
        if (dom.isEmpty()) throw new Exception("Dominio requerido.");
        v.setDominio(dom);
        String anio = txtAnio.getText().trim();
        v.setAnio(anio.isEmpty()? null : Integer.parseInt(anio));
        v.setMarca(txtMarca.getText().trim());
        v.setModelo(txtModelo.getText().trim());
        v.setColor(txtColor.getText().trim());
        return v;
    }

    private void limpiarForm() {
        txtDominio.setText(""); txtAnio.setText(""); txtMarca.setText("");
        txtModelo.setText(""); txtColor.setText("");
        txtDominio.setEditable(true);
        tblVehiculos.clearSelection();
        txtDominio.requestFocus();
    }
    
